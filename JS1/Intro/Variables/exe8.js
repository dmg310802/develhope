// Type Conversion

let typeC = true;

console.log(typeC);
console.log(typeof(typeC));

//Number
console.log(Number(typeC));
console.log(typeof(Number(typeC)));

//N to String
console.log(String(Number(typeC)));
console.log(typeof(String(Number(typeC))));

// N to Str to Boolean
console.log(Boolean(String(Number(typeC))));
console.log(typeof(Boolean(String(Number(typeC)))));


